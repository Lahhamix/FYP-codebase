package com.example.ble_viewer

import android.util.Log
import javax.crypto.Cipher
import javax.crypto.spec.IvParameterSpec
import javax.crypto.spec.SecretKeySpec

object AESCrypto {

    private const val TAG = "AESCrypto"

    // Keys and IVs must match the Arduino code exactly.
    private val accelKey = byteArrayOf(0x41, 0x63, 0x63, 0x65, 0x6C, 0x4B, 0x65, 0x79, 0x31, 0x32, 0x33, 0x34, 0x35, 0x36, 0x37, 0x38)
    private val accelIV = byteArrayOf(0x00, 0x01, 0x02, 0x03, 0x04, 0x05, 0x06, 0x07, 0x08, 0x09, 0x0A, 0x0B, 0x0C, 0x0D, 0x0E, 0x0F)

    private val gyroKey = byteArrayOf(0x47, 0x79, 0x72, 0x6F, 0x4B, 0x65, 0x79, 0x31, 0x32, 0x33, 0x34, 0x35, 0x36, 0x37, 0x38, 0x39)
    private val gyroIV = byteArrayOf(0x10, 0x11, 0x12, 0x13, 0x14, 0x15, 0x16, 0x17, 0x18, 0x19, 0x1A, 0x1B, 0x1C, 0x1D, 0x1E, 0x1F)

    private val heartRateKey = byteArrayOf(0x48, 0x65, 0x61, 0x72, 0x74, 0x52, 0x61, 0x74, 0x65, 0x4B, 0x65, 0x79, 0x31, 0x32, 0x33, 0x34)
    private val heartRateIV = byteArrayOf(0x20, 0x21, 0x22, 0x23, 0x24, 0x25, 0x26, 0x27, 0x28, 0x29, 0x2A, 0x2B, 0x2C, 0x2D, 0x2E, 0x2F)

    private val spo2Key = byteArrayOf(0x53, 0x70, 0x4F, 0x32, 0x4B, 0x65, 0x79, 0x31, 0x32, 0x33, 0x34, 0x35, 0x36, 0x37, 0x38, 0x39)
    private val spo2IV = byteArrayOf(0x30, 0x31, 0x32, 0x33, 0x34, 0x35, 0x36, 0x37, 0x38, 0x39, 0x3A, 0x3B, 0x3C, 0x3D, 0x3E, 0x3F)

    private fun decryptWithKeyIV(encryptedBytes: ByteArray, key: ByteArray, iv: ByteArray): String {
        return try {
            if (encryptedBytes.isEmpty()) return "DECRYPT_ERROR: EMPTY_INPUT"
            
            val cipher = Cipher.getInstance("AES/CBC/PKCS5Padding")
            val keySpec = SecretKeySpec(key, "AES")
            val ivSpec = IvParameterSpec(iv)
            cipher.init(Cipher.DECRYPT_MODE, keySpec, ivSpec)
            val decryptedBytes = cipher.doFinal(encryptedBytes)
            String(decryptedBytes, Charsets.UTF_8).trim()
        } catch (e: Exception) {
            Log.e(TAG, "Decryption failed for ${encryptedBytes.size} bytes.", e)
            "DECRYPT_ERROR"
        }
    }

    fun decryptAccel(encryptedBytes: ByteArray): String = decryptWithKeyIV(encryptedBytes, accelKey, accelIV)
    fun decryptGyro(encryptedBytes: ByteArray): String = decryptWithKeyIV(encryptedBytes, gyroKey, gyroIV)
    fun decryptHeartRate(encryptedBytes: ByteArray): String = decryptWithKeyIV(encryptedBytes, heartRateKey, heartRateIV)
    fun decryptSpO2(encryptedBytes: ByteArray): String = decryptWithKeyIV(encryptedBytes, spo2Key, spo2IV)
}